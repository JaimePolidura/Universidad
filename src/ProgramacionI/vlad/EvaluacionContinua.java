    public static void teclaUsuario () {
        Scanner userInput = new Scanner(System.in);
        String teclaPulsada = userInput.nextLine();

        if(teclaPulsada.equalsIgnoreCase("w") || teclaPulsada.equalsIgnoreCase("a") || teclaPulsada.equalsIgnoreCase("s") || teclaPulsada.equalsIgnoreCase("d")) {
            int nuevaPosicionFilas = posicionXJugador;
            int nuevaPosicionColumans = posicionYJugador;

            if(teclaPulsada.equalsIgnoreCase("w")) {nuevaPosicionFilas--;}
            else if(teclaPulsada.equalsIgnoreCase("s")) {nuevaPosicionFilas++;}
            else if(teclaPulsada.equalsIgnoreCase("d")) {nuevaPosicionColumans++;}
            else if(teclaPulsada.equalsIgnoreCase("a")) {nuevaPosicionColumans--;}


            //Comprabamos que el jugador no se salga del mapa y que no vaya a caminar hacia un bloque no permitido (con una id inpar)
            if(!(nuevaPosicionFilas + 1 > mundo.length || nuevaPosicionFilas < 0 || nuevaPosicionColumans + 1 > mundo[0].length || nuevaPosicionColumans < 0) && mundo[nuevaPosicionFilas][nuevaPosicionColumans] % 2 == 0) {
                posicionXJugador = nuevaPosicionFilas;
                posicionYJugador = nuevaPosicionColumans;
            }

        }else{
            if(teclaPulsada.equalsIgnoreCase("f")){
                jugando =false;
            } else if (teclaPulsada.equalsIgnoreCase("v")){
                viewport=userInput.nextInt();
                System.out.print("\033[H\033[2J");
                System.out.flush();
            }
        }
    }

